-module(grepper_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    {_, FilePath} = application:get_env(grepper, file),
    file_handler:handle(FilePath),
    grepper_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

